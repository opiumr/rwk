<img src="images/army/v6.jpg" width="440" alt="" title="" style="border-radius: 20px" tabindex="0"/>
<p>Член правління ПАТ «Укрзалізниця» Сергій Михальчук повідомив, що залізничники продовжать приймати пропозиції від військових, особливо за результатами поїздок у рейси, коли вагон почне курсувати в складі поїзда. Відгуки та побажання будуть враховані при облаштуванні інших вагонів для військової варти. «Модернізацію вагона Укрзалізниця профінансувала власним коштом. Ми прагнемо, щоб військовим було зручно, для цього створили відповідні умови», - зазначив член правління ПАТ «Укрзалізниця».</p>
