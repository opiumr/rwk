<h2><a name="28_04_2018">У філії «Рефрижераторна вагонна компанія» ПАТ «Укрзалізниця» підбито підсумки конкурсу дитячого малюнка «Охорона праці очима дітей» з нагоди Дня охорони праці у 2018 році</a></h2>
<p>Щорічно 28 квітня понад 100 країн світу відзначають Всесвітній день охорони праці.</p>
<p>У 2018 році тема Всесвітнього дня охорони праці за рекомендацією Міжнародної організації праці: «Захищене і здорове покоління».</p>
<p>З нагоди проведення Всесвітнього дня охорони праці у філії «РВК» відбувся огляд-конкурс дитячого малюнка «Охорона праці очима дітей». У конкурсі приймали учаcть діти та внуки працівників нашого підприємства, майбутні залізничники. У своїх малюнках діти відобразили зацікавленість до питань збереження життя та здоров’я працівників та розуміння важливості безпечної праці. Роботи дітей демонструють бездоганні знання правил безпеки під час перебування на залізничних коліях. Авторів гарних малюнків було заохочено цінними подарунками від профспілкового комітету філії.</p>
<p>Малюнки дітей свідчать про активну роботу в пропаганді охорони особистої безпеки серед дітей та школярів та попередженні випадків невиробничого травматизму. Проведення таких заходів наочно демонструє дітям та їх батькам надзвичайну важливість дотримання правил особистої безпеки на об’єктах залізничної інфраструктури.</p>
