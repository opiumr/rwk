<h2><a name="18_05_2017">18.05.2017 року - День Вишиванки</a></h2>
<img src="images/vishivanka/day.jpg" width="960" alt="" title="">
<p>18 травня 2017 року колектив філії «Рефрижераторна вагонна компанія ПАТ «Укрзалізниця» зустрів ранок з піднесеним настроєм з веселими посмішками, в українських вишиванках.</p>
