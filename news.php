<h2><a name="11_07_2017">Укрзалізниця продемонструвала модернізований вагон для військової варти</a></h2>
<p>10 липня, член правління ПАТ «Укрзалізниця» Сергій Михальчук презентував модернізований вагон для перевезення військової варти. Роботи виконала філія «Рефрижераторна вагонна компанія» ПАТ «Укрзалізниця». На його переобладнання зі службово-технічного рефрижераторного вагона було спрямовано понад 200 тисяч грн. У заході взяли участь представники Міністерства оборони України та заступник міністра інфраструктури України Юрій Лавренюк.</p>
<img src="images/army/v.jpg" width="440" alt="" title="" style="border-radius: 20px" tabindex="0"/>
<p>Метою модернізації вагона було покращення умов перевезення вартових, які супроводжують військові ешелони. Залізничники виконали роботи відповідно до доручення Уряду.</p>
<p>Службово-технічний рефрижераторний вагон переобладнали на 60%. Тепер у вагоні для варти обладнане спальне купе, караульне приміщення, санвузол та кухня з меблями, кухонним інвентарем і плитою. Усі побутові приміщення взимку опалюватимуться. Система водозабезпечення – аналогічна системі пасажирського вагона. У спецвагоні є душ, умивальник і туалет. Також передбачений сейф для зброї, грати на вікнах та інше відповідно до військового уставу караульної служби.</p>
<img src="images/army/v3.jpg" width="440" alt="" title="" style="border-radius: 20px" tabindex="0"/>
<img src="images/army/v7.jpg" width="440" alt="" title="" style="border-radius: 20px" tabindex="0"/>
