<h2><a name="01_03_2018">Висловіть свою думку щодо єдиного порядку виділення путівок на оздоровлення працівників Укрзалізниці!</a></h2>
<p>На початку цього року ПАТ «Укрзалізниця» ініціювало розробку єдиного порядку виділення путівок на оздоровлення та лікування своїх працівників. Необхідність унормування цього процесу обумовлена наступним. Сьогодні усі філії та регіональні філії хоч і інтегровані в Товариство, фактично — як це історично склалось – продовжують застосовувати власні норми щодо розподілу путівок. Ось і виходить, що в одному підрозділі залізничникам компенсують 90% вартості оздоровлення, а в іншому — лише 70%. При цьому відпочивати вони та члени їх родин можуть в одному й тому ж санаторії. Також при розподілі часто діють багато інших суб’єктивних факторів.</p>
<p>Тож правління ПАТ «Укрзалізниця» вирішило забезпечити «соціальну рівність» працівників залізничної компанії. В результаті трудовий колектив ПАТ «Укрзалізниця» має отримати новий ефективний механізм виділення путівок, що буде враховувати інтереси всіх працівників Товариства та профспілок. Основними принципами такого механізму стануть рівність, прозорість, єдність та підзвітність. Але розробляючи нові правила в цій соціально чутливій сфері, важливо врахувати думки працівників компанії, адже саме на максимальне задоволення їх потреб вони й спрямовані.</p>
<p>Запрошуємо працівників ПАТ «Укрзалізниця» взяти участь в обговоренні та зазначити питання, які на Вашу думку, необхідно висвітлити у єдиному порядку виділення путівок на оздоровлення та надсилати свої зауваження і пропозиції  на зазначені нижче електронні адреси:</p>	
